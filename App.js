import { NewAppScreen } from '@react-native/new-app-screen';
import { Platform, StatusBar, StyleSheet, useColorScheme, View } from 'react-native';
import {
  SafeAreaProvider,
  SafeAreaView,
  useSafeAreaInsets,
} from 'react-native-safe-area-context';
import Navigation from './src/navigation';
import Spinner from './src/components/Spinner'
import { useState, createContext, useEffect } from 'react';
import Toast from 'react-native-toast-message';
import { COLORS } from './src/screens/auth/BoardingScreen';


export const LoadContext = createContext('');
export const UserContext = createContext('');
function App() {
  const isDarkMode = useColorScheme() === 'light';
  const [loading, setLoading] = useState(false);
  const [user, setuser] = useState({});





  return (
    <SafeAreaProvider>
      <StatusBar barStyle={isDarkMode ? 'light-content' : 'dark-content'} />
      <SafeAreaView style={[styles.container]} edges={Platform.OS === 'ios' ? ['left', 'right'] : ['bottom', 'left', 'right', 'top']} >
        <UserContext.Provider value={[user, setuser]}>
          <LoadContext.Provider value={[loading, setLoading]}>
            <Spinner isLoading={loading} />
            <Navigation initial='Boarding' />
          </LoadContext.Provider>
        </UserContext.Provider>
      </SafeAreaView>
      <Toast />
    </SafeAreaProvider>
  );
}



const styles = StyleSheet.create({
  container: {
    flex: 1,
    // backgroundColor: COLORS.primary
  },
});

export default App;
